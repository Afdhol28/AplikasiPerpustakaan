    private void AutoNumber(){
        
        try {
            Connection koneksi1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/perpus","root","");
            Statement st = koneksi1.createStatement();
            String sql = "select max(right(id_anggota,3)) as kode from tb_anggota";      
            ResultSet res = st.executeQuery(sql);
            if (res.next()) {
                String no_auto, nol_plus;
                int p; 
                no_auto = Integer.toString(res.getInt(1)+1);
                p = no_auto.length();
                nol_plus = "";
                for(int i=1;i<=3-p;i++){
                nol_plus = nol_plus +"0";
                }
                txt_id_anggota.setText("A"+nol_plus+no_auto);
            }
            
            
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

    }
