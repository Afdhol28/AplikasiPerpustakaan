    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        DefaultTableModel table1;
        table1 = new DefaultTableModel();
        table1.addColumn("ID ANGGOTA");
        table1.addColumn("NAMA ANGGOTA");
        table1.addColumn("NIS");
        table1.addColumn("KELAS");
        table1.addColumn("JENIS KELAMIN");
        table1.addColumn("ALAMAT");
        try
        {
            Statement st = koneksi1.statement;
            String search = txt_cari.getText();
            ResultSet rs = st.executeQuery("SELECT * FROM tb_anggota WHERE id_anggota like '%"+search+"%'");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

            dtm.setRowCount(0);
            String [] data = new String[6];
            int i = 1;

            while(rs.next())
            {
                data[0] = rs.getString("id_anggota");
                data[1] = rs.getString("nama_anggota");
                data[2] = rs.getString("nis");
                data[3] = rs.getString("kelas");
                data[4] = rs.getString("kelamin");
                data[5] = rs.getString("alamat");
                dtm.addRow(data);
                i++;
            }
            rs.close();
            txt_cari.setText("");
        }
        catch(Exception ex)  {
            JOptionPane.showMessageDialog(null,"Data yang Anda cari Tidak dapat Ditemukan");
            System.err.println("error (search) : " +ex);
        }
    }//GEN-LAST:event_btn_cariActionPerformed

